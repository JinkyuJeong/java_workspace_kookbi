package chap3;

public class OpEx4 {
	public static void main(String[] args) {
		int x=1, y=1;
		System.out.println("1 == 1 : "+(x==y));
		System.out.println("1 != 1 : "+(x!=y));
		System.out.println("1 > 1 : "+(x>y));
		System.out.println("1 >= 1 : "+(x>=y));
		
		System.out.println("x==1 && y==2 : " + (x==1 && y==2));
		System.out.println("x==1 || y==2 : " + (x==1 || y==2));
	}

}
