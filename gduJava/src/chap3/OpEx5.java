package chap3;

public class OpEx5 {
	public static void main(String[] args) {
		System.out.println(6&3);
		System.out.println(6|3);
		System.out.println(6^3);
		System.out.println(~10);
		
		System.out.println("true & true : " + (true & true));
		System.out.println("true & false : " + (true & false));
	}

}
