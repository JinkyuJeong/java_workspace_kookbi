package chap3;

public class OpEx2 {
	public static void main(String[] args) {
		System.out.println("true="+!false);
		System.out.println("false="+!true);
		System.out.println("5 != 4 :" +(5 != 4));
		System.out.println("5 == 4 :" +(5 == 4));
		
		int x = -10;
		System.out.println("+x= "+ +x);
		System.out.println("-x= "+ -x);
		x = 10;
		System.out.println("+x= "+ +x);
		System.out.println("-x= "+ -x);
	}

}
